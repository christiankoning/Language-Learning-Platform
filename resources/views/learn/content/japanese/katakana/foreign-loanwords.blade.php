    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Foreign Loanwords in Katakana</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Katakana is commonly used to write foreign (non-Japanese) words, especially English words that have been adopted into Japanese. These are known as <strong>gairaigo</strong> (外来語). Understanding how foreign words are adapted can help you both recognize familiar vocabulary and pronounce it in a natural Japanese way.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Long Vowels with ー</h2>
        <p class="mb-4">
            Long vowel sounds in Katakana are written using a special symbol: the <strong>chōonpu</strong> (ー). It extends the vowel sound of the previous character.
        </p>
        <ul class="list-disc list-inside mb-6">
            <li>ケーキ (kēki) = cake</li>
            <li>メール (mēru) = email</li>
            <li>ゲーム (gēmu) = game</li>
        </ul>
        <p class="mb-4">
            This is different from Hiragana, which usually uses separate vowel kana (like あ or う) to indicate length. In Katakana, always use ー to extend a vowel sound.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Small ッ (Sokuon)</h2>
        <p class="mb-4">
            The small ッ (sokuon) is used to indicate a doubled consonant sound (gemination), which creates a pause or emphasis before the consonant.
        </p>
        <ul class="list-disc list-inside mb-6">
            <li>バッグ (baggu) = bag</li>
            <li>カップ (kappu) = cup</li>
            <li>チケット (chiketto) = ticket</li>
        </ul>
        <p class="mb-4">
            You’ll often see it in words that come from English words with double consonants, such as "coffee cup" becoming コーヒーカップ.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Extended Katakana Sounds</h2>
        <p class="mb-4">
            Some foreign sounds that don’t exist in native Japanese require special Katakana combinations:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>ヴ</strong> is used for the "v" sound, although it's often approximated as "b". Example: ヴァイオリン (vaiorin = violin).</li>
            <li>Small vowels are used to form combinations like ファ (fa), フィ (fi), フェ (fe), フォ (fo). Example: ファッション (fasshon) = fashion.</li>
            <li>ティ (ti) and ディ (di) are used for words like パーティー (pātī = party) and ディナー (dinā = dinner).</li>
        </ul>
        <p class="mb-4">
            These combinations help Katakana more accurately represent modern foreign loanwords.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Examples of Foreign Loanwords</h2>
        <ul class="list-disc list-inside mb-6">
            <li>テレビ (terebi) = television</li>
            <li>コンピューター (konpyūtā) = computer</li>
            <li>アイスクリーム (aisukurīmu) = ice cream</li>
            <li>エレベーター (erebētā) = elevator</li>
        </ul>

        <p>
            When reading or writing Katakana, listen for how the word sounds in Japanese, not how it is spelled in the original language. Pay close attention to vowel extensions, double consonants, and unique sounds like ヴ or small vowels!
        </p>
    </div>
</section>
