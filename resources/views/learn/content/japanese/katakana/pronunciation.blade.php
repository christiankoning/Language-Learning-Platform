    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Katakana Pronunciation
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Katakana represents the same basic 46 sounds as Hiragana. Each Katakana character corresponds to a syllable made up of a consonant + vowel (or just a vowel).
        </p>

        <p class="mb-4">
            Unlike English, Japanese syllables are consistent in pronunciation. There are no silent letters, and each sound is spoken clearly and evenly.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Examples:</h2>
        <ul class="list-disc list-inside mb-6">
            <li>ア (a) – like the "a" in “car”</li>
            <li>キ (ki) – like “key”</li>
            <li>ス (su) – like “soon” (but short)</li>
            <li>ネ (ne) – like “net”</li>
            <li>ホ (ho) – like “ho” in “hotel”</li>
        </ul>

        <p>
            Practice speaking each character aloud slowly. You’ll build rhythm and confidence as you go!
        </p>
    </div>
