<section class="w-full max-w-5xl mx-auto px-6 py-12 text-white">
    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Katakana: Pronunciation
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Katakana uses the same 46 basic syllables as Hiragana, but it's primarily used for foreign words, names, onomatopoeia, and some technical or scientific terms. Each character represents a consistent sound, making pronunciation relatively straightforward.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Japanese Vowels</h2>
        <p class="mb-4">
            Katakana is built on five core vowels:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>ア (a)</strong> — like "ah" in "car"</li>
            <li><strong>イ (i)</strong> — like "ee" in "see"</li>
            <li><strong>ウ (u)</strong> — like "oo" in "food", but shorter and softer</li>
            <li><strong>エ (e)</strong> — like "e" in "get"</li>
            <li><strong>オ (o)</strong> — like "o" in "more", but shorter</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Unvoiced Vowels (Silent Sounds)</h2>
        <p class="mb-4">
            In natural Japanese speech — even in foreign words written in Katakana — the vowels <strong>ウ (u)</strong> and <strong>イ (i)</strong> may become very quiet or silent depending on the context.
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>パーティー (paatii)</strong> — "Party": the final イ may be lightly pronounced or softened</li>
            <li><strong>サンドイッチ (sandoicchi)</strong> — "Sandwich": the ウ is often extremely short</li>
            <li><strong>バッグ (baggu)</strong> — "Bag": the final ウ in グ is barely audible</li>
        </ul>
        <p class="mb-4">
            This is called <strong>unvoiced vowels</strong> (無声母音, musei boin) and occurs in casual or rapid speech. The spelling stays the same.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Lengthened Vowels in Katakana</h2>
        <p class="mb-4">
            In Katakana, long vowels are not written by repeating characters. Instead, the symbol <strong>ー</strong> is used to extend the vowel sound.
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>ゲーム (geemu)</strong> — "Game"</li>
            <li><strong>コーヒー (koohii)</strong> — "Coffee"</li>
            <li><strong>スーパー (suupaa)</strong> — "Supermarket"</li>
        </ul>
        <p class="mb-4">
            The long vowel mark always follows the character and indicates that the previous vowel is held longer.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Practice Tips</h2>
        <ul class="list-disc list-inside mb-6">
            <li>Say Katakana words out loud — many are based on English, so you'll recognize them.</li>
            <li>Pay attention to long vowels and unvoiced sounds when listening to Japanese speech.</li>
            <li>Try reading food packaging, menus, or signs in Katakana while in Japan or online.</li>
        </ul>

        <p class="mb-4">
            Once you're confident with pronunciation, you'll be ready to build and read full words in Katakana.
        </p>
    </div>
</section>
