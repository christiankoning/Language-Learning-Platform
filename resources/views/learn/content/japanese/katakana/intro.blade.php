    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Introduction to Katakana
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Katakana (カタカナ) is one of the three scripts used in written Japanese. It is mainly used for:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li>Foreign loanwords (like コンピューター — computer)</li>
            <li>Foreign names (ジョン — John)</li>
            <li>Onomatopoeia (ドキドキ — heartbeat sound)</li>
            <li>Scientific or technical terms</li>
            <li>Emphasis (similar to italics in English)</li>
        </ul>

        <p class="mb-4">
            Katakana characters represent the same sounds as Hiragana, but their appearance is more angular and sharp.
        </p>

        <p class="mb-4">
            Learning Katakana is essential to reading signs, menus, and modern media in Japan. It's often introduced after mastering Hiragana.
        </p>

        <p>
            In the next sections, you'll learn how Katakana is pronounced, how each character looks, and how to read and write them confidently.
        </p>
    </div>
