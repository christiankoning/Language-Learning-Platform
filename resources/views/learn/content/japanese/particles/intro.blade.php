    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Particles: Introduction</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Particles are one of the most essential parts of Japanese grammar. They are small words that follow nouns, verbs, adjectives, or even entire phrases to show their relationship to the rest of the sentence.
        </p>

        <p class="mb-4">
            Think of particles as markers that show the <strong>function or role</strong> of a word in a sentence—like subject, object, location, direction, and more. While English uses word order and prepositions, Japanese relies heavily on particles to communicate the same ideas.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">What Are Particles?</h2>
        <p class="mb-4">
            Particles are typically placed <strong>after</strong> the word they modify. For example:
        </p>

        <p class="mb-4 italic">私は学生です。<br>watashi <strong>wa</strong> gakusei desu.<br>→ I am a student.</p>

        <p class="mb-4">
            In this sentence:
        </p>
        <ul class="list-disc ml-6 mb-4">
            <li><strong>は (wa)</strong> marks the topic (“I” in this case).</li>
            <li><strong>です</strong> is the polite copula (“is/am/are”).</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Why Are Particles Important?</h2>
        <p class="mb-4">
            Because Japanese has flexible word order, <strong>particles tell you what each word is doing</strong> in a sentence. Even if the subject or object moves around, the particle makes its function clear.
        </p>

        <p class="mb-4">
            Mastering particles is critical for understanding and forming natural Japanese sentences.
        </p>

