    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Particles: Additional Particles</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Beyond the core particles, Japanese has many other particles that add nuance or serve specific grammatical functions. This section introduces several important additional particles to deepen your understanding.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">1. へ (e) – Directional Particle</h2>
        <p class="mb-2">
            Indicates direction or destination, similar to に but with more emphasis on the movement toward something.
        </p>
        <p class="italic mb-4">学校へ行きます。<br><span class="text-sm">Gakkou e ikimasu. → I go to school.</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">2. や (ya) – “And” (non-exhaustive list)</h2>
        <p class="mb-2">
            Connects items in a list, implying there may be more items not mentioned.
        </p>
        <p class="italic mb-4">りんごやバナナを食べました。<br><span class="text-sm">Ringo ya banana o tabemashita. → I ate apples and bananas (among other things).</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">3. ね (ne) – Confirmation / Soft Tag</h2>
        <p class="mb-2">
            Used at the end of a sentence to seek agreement or confirm something with the listener (like “isn't it?” or “right?”).
        </p>
        <p class="italic mb-4">今日は寒いですね。<br><span class="text-sm">Kyou wa samui desu ne. → It's cold today, isn't it?</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">4. よ (yo) – Emphasis / Assertion</h2>
        <p class="mb-2">
            Adds emphasis to a statement, as if informing or declaring something new to the listener.
        </p>
        <p class="italic mb-4">これは美味しいよ！<br><span class="text-sm">Kore wa oishii yo! → This is delicious!</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">5. だけ (dake) – “Only”</h2>
        <p class="mb-2">
            Indicates limitation, meaning "only" or "just."
        </p>
        <p class="italic mb-4">一人だけ来ました。<br><span class="text-sm">Hitori dake kimashita. → Only one person came.</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">6. しか (shika) – “Only” (with negative verb)</h2>
        <p class="mb-2">
            Also means "only," but it must be used with a negative verb. It emphasizes that nothing else is involved.
        </p>
        <p class="italic mb-4">水しか飲みません。<br><span class="text-sm">Mizu shika nomimasen. → I drink only water (nothing else).</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">7. でも (demo) – “But” / “Even” / “Also”</h2>
        <p class="mb-2">
            Used to mean "but" (at the beginning of a sentence), or "even" / "also" depending on context.
        </p>
        <p class="italic mb-4">お茶でも飲みましょうか？<br><span class="text-sm">Ocha demo nomimashou ka? → Shall we have tea or something?</span></p>
        <p class="italic mb-4">高くても買います。<br><span class="text-sm">Takakute mo kaimasu. → Even if it's expensive, I will buy it.</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">8. ほど (hodo) – “To the extent” / Comparison</h2>
        <p class="mb-2">
            Indicates degree, comparison, or limit. Often used in comparisons to mean “as much as” or “not as... as.”
        </p>
        <p class="italic mb-4">彼ほど背が高くない。<br><span class="text-sm">Kare hodo se ga takakunai. → I’m not as tall as him.</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">9. ながら (nagara) – “While Doing…”</h2>
        <p class="mb-2">
            Connects two actions happening at the same time. Attach it to the stem of the first verb.
        </p>
        <p class="italic mb-4">音楽を聞きながら勉強します。<br><span class="text-sm">Ongaku o kikinagara benkyou shimasu. → I study while listening to music.</span></p>

        <h2 class="text-2xl font-semibold mt-10 mb-4">Conclusion</h2>
        <p class="mb-4">
            These additional particles enrich your Japanese and allow you to express more subtle ideas. Mastering them will help you sound more natural and fluent in conversation.
        </p>

