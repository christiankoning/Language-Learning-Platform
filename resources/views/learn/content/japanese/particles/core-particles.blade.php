    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Particles: Core Particles</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Particles are short words in Japanese that indicate the relationship between words in a sentence. Mastering these core particles is essential for understanding and forming correct Japanese sentences.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Essential Core Particles</h2>

        <h3 class="text-xl font-semibold mt-6 mb-2">は (wa) – Topic Marker</h3>
        <p class="mb-2">Marks the topic of the sentence.</p>
        <p class="italic mb-4">わたしは学生です。<br><span class="text-sm">Watashi wa gakusei desu. → I am a student.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">が (ga) – Subject Marker</h3>
        <p class="mb-2">Marks the subject, especially when introducing new information or emphasizing it.</p>
        <p class="italic mb-4">だれが来ますか？<br><span class="text-sm">Dare ga kimasu ka? → Who is coming?</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">を (o) – Object Marker</h3>
        <p class="mb-2">Marks the direct object of a verb.</p>
        <p class="italic mb-4">パンを食べます。<br><span class="text-sm">Pan o tabemasu. → I eat bread.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">に (ni) – Time / Destination Marker</h3>
        <p class="mb-2">Indicates time, destination, or indirect object.</p>
        <p class="italic mb-4">6時に起きます。<br><span class="text-sm">Roku-ji ni okimasu. → I wake up at 6.</span></p>
        <p class="italic mb-4">学校に行きます。<br><span class="text-sm">Gakkou ni ikimasu. → I go to school.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">で (de) – Place of Action Marker</h3>
        <p class="mb-2">Marks where an action takes place.</p>
        <p class="italic mb-4">図書館で勉強します。<br><span class="text-sm">Toshokan de benkyou shimasu. → I study at the library.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">と (to) – “And” / “With”</h3>
        <p class="mb-2">Used to list items or indicate doing something with someone.</p>
        <p class="italic mb-4">友達と話します。<br><span class="text-sm">Tomodachi to hanashimasu. → I talk with a friend.</span></p>
        <p class="italic mb-4">りんごとバナナを食べました。<br><span class="text-sm">Ringo to banana o tabemashita. → I ate an apple and a banana.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">も (mo) – “Also” / “Too”</h3>
        <p class="mb-2">Replaces は, が, or を to mean "also" or "too".</p>
        <p class="italic mb-4">私も学生です。<br><span class="text-sm">Watashi mo gakusei desu. → I am also a student.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">の (no) – Possessive Marker</h3>
        <p class="mb-2">Shows possession or connection between two nouns.</p>
        <p class="italic mb-4">彼の車です。<br><span class="text-sm">Kare no kuruma desu. → It’s his car.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">から (kara) – “From” / “Since”</h3>
        <p class="mb-2">Indicates the starting point in space or time.</p>
        <p class="italic mb-4">9時から働きます。<br><span class="text-sm">Ku-ji kara hatarakimasu. → I work from 9 o’clock.</span></p>

        <h3 class="text-xl font-semibold mt-6 mb-2">まで (made) – “Until” / “Up to”</h3>
        <p class="mb-2">Indicates the ending point in space or time.</p>
        <p class="italic mb-4">5時まで勉強します。<br><span class="text-sm">Go-ji made benkyou shimasu. → I study until 5 o’clock.</span></p>

        <h2 class="text-2xl font-semibold mt-10 mb-4">Conclusion</h2>
        <p class="mb-4">
            These 10 particles form the backbone of Japanese grammar. Once you become familiar with them, forming natural sentences becomes much easier. Don’t worry if you make mistakes at first—practice and exposure will help!
        </p>

