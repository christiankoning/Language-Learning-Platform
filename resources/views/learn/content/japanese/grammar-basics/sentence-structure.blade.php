        <h2 class="text-2xl font-semibold mt-8 mb-4">Understanding Particles</h2>
        <p class="mb-4">
            Japanese uses small words called <strong>particles</strong> to indicate the role of words in a sentence. The most common particles you’ll see in basic sentence structure are:
        </p>

        <ul class="list-disc ml-6 mb-4">
            <li><strong>は (wa)</strong> – Topic marker</li>
            <li><strong>を (o)</strong> – Direct object marker</li>
            <li><strong>が (ga)</strong> – Subject marker (used for emphasis or new subjects)</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Word Order Flexibility</h2>
        <p class="mb-4">
            Although Japanese sentences often follow a standard structure, word order is flexible as long as the verb comes at the end. This is thanks to particles, which indicate each word’s role regardless of its position.
        </p>

