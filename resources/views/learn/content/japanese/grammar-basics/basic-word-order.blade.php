    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Grammar Basics: Basic Word Order</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Understanding Japanese word order is essential for creating sentences that sound natural. Unlike English, which follows a Subject-Verb-Object (SVO) structure, Japanese typically uses <strong>Subject-Object-Verb (SOV)</strong>.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Standard Word Order in Japanese</h2>
        <p class="mb-4">
            The basic order of a Japanese sentence is:
        </p>
        <ul class="list-disc ml-6 mb-4">
            <li><strong>Subject</strong> (topic of the sentence)</li>
            <li><strong>Object</strong> (what is being acted upon)</li>
            <li><strong>Verb</strong> (action)</li>
        </ul>

        <p class="mb-4">
            Let’s look at an example:
        </p>
