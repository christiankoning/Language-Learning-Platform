        <h2 class="text-2xl font-semibold mt-8 mb-4">Expanded Sentence Patterns</h2>
        <p class="mb-4">
            These common sentence structures help you build up more natural Japanese step-by-step:
        </p>

        <ul class="list-disc ml-6 mb-4">
            <li><strong>Topic + は + Verb</strong> — <code>私は行きます。</code> → I go.</li>
            <li><strong>Topic + は + Object + を + Verb</strong> — <code>私はパンを食べます。</code> → I eat bread.</li>
            <li><strong>Topic + は + Place + で + Verb</strong> — <code>私は学校で勉強します。</code> → I study at school.</li>
            <li><strong>Topic + は + Time + に + Verb</strong> — <code>私は七時に起きます。</code> → I wake up at 7.</li>
            <li><strong>Topic + は + Time + に + Place + で + Object + を + Verb</strong><br><code>私は朝七時に学校でパンを食べます。</code> → I eat bread at school at 7 in the morning.</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Optional Subjects</h2>
        <p class="mb-4">
            In casual or obvious contexts, the subject is often omitted. For example:
        </p>
        <p class="mb-4 italic">パンを食べます。 → (I) eat bread.</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Modifiers</h2>
        <p class="mb-4">
            Descriptive words (like adjectives or clauses) come <strong>before</strong> the word they modify. For example:
        </p>
        <p class="mb-4 italic">おいしいパンを食べます。→ I eat delicious bread.</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Common Particles in Word Order</h2>
        <ul class="list-disc ml-6 mb-4">
            <li><strong>は</strong> — Topic marker</li>
            <li><strong>を</strong> — Direct object marker</li>
            <li><strong>で</strong> — Indicates location of action</li>
            <li><strong>に</strong> — Indicates time or destination</li>
        </ul>

