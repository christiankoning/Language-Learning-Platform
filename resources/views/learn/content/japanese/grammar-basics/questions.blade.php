        <h2 class="text-2xl font-semibold mt-8 mb-4">Using Question Words</h2>
        <p class="mb-4">
            Japanese uses question words like <strong>何 (nani – what)</strong>, <strong>誰 (dare – who)</strong>, <strong>どこ (doko – where)</strong>, and <strong>いつ (itsu – when)</strong>.
        </p>

        <h3 class="text-xl font-semibold mt-6 mb-2">Examples:</h3>
        <ul class="list-disc ml-6 mb-4">
            <li><strong>何ですか？</strong> – <em>nani desu ka?</em> – What is it?</li>
            <li><strong>誰ですか？</strong> – <em>dare desu ka?</em> – Who is it?</li>
            <li><strong>どこですか？</strong> – <em>doko desu ka?</em> – Where is it?</li>
            <li><strong>いつ行きますか？</strong> – <em>itsu ikimasu ka?</em> – When will (you) go?</li>
        </ul>

