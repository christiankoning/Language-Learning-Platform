        <h2 class="text-2xl font-semibold mt-8 mb-4">Optional Omission of Subject</h2>
        <p class="mb-4">
            As with positive sentences, the subject is often omitted when it’s clear from context.
        </p>
        <p class="mb-4 italic">食べません。 → (I) don’t eat.</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Negative Questions</h2>
        <p class="mb-4">
            To ask a question in the negative, just add <strong>か</strong> at the end of the negative verb.
        </p>
        <p class="mb-4 italic">行きませんか？ → Won’t (you) go?</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Conclusion</h2>
        <p class="mb-4">
            Negating verbs and statements in Japanese is straightforward once you know how to modify <strong>です</strong> and <strong>〜ます</strong>. These forms are essential to express what you or others don’t do.
        </p>

