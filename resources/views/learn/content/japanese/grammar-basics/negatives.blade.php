        <h2 class="text-2xl font-semibold mt-8 mb-4">Negative Verbs: 〜ません Form</h2>
        <p class="mb-4">
            To make a verb negative in polite form, change the ending <strong>〜ます</strong> to <strong>〜ません</strong>.
        </p>

