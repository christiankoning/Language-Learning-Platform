    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Grammar Basics: Negatives</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            In Japanese, you can make negative sentences by changing the verb or copula to its negative form. This allows you to say things like “I don’t eat” or “It’s not a cat.”
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Negative Copula: ではありません / じゃないです</h2>
        <p class="mb-4">
            The copula <strong>です</strong> becomes <strong>ではありません</strong> (formal) or <strong>じゃないです</strong> (casual polite) in the negative form.
        </p>

