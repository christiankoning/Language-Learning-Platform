    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Grammar Basics: Copulas</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            In Japanese, a <strong>copula</strong> functions like the English verb “to be” (am, is, are). The most common copula is <strong>です (desu)</strong>, which links a subject with a noun or adjective to make a statement or description.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Using です (Desu)</h2>
        <p class="mb-4">
            <code>です</code> is used at the end of a sentence to show politeness and link the topic to a description or identity:
        </p>
        <div class="overflow-x-auto mb-6">
            <table class="table-auto border border-white/20 text-left w-full">
                <thead>
                <tr>
                    <th class="border border-white/20 px-3 py-2">Japanese</th>
                    <th class="border border-white/20 px-3 py-2">Romaji</th>
                    <th class="border border-white/20 px-3 py-2">Meaning</th>
                </tr>
                </thead>
                <tbody>
                <tr>
                    <td class="border border-white/20 px-3 py-2">これはペンです。</td>
                    <td class="border border-white/20 px-3 py-2">kore wa pen desu</td>
                    <td class="border border-white/20 px-3 py-2">This is a pen.</td>
                </tr>
                <tr>
                    <td class="border border-white/20 px-3 py-2">私は学生です。</td>
                    <td class="border border-white/20 px-3 py-2">watashi wa gakusei desu</td>
                    <td class="border border-white/20 px-3 py-2">I am a student.</td>
                </tr>
                </tbody>
            </table>
        </div>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Negative Form: じゃありません / ではありません</h2>
        <p class="mb-4">
            To say something is not something, use the negative forms:
        </p>
        <ul class="list-disc ml-6 mb-4">
            <li><code>じゃありません</code> — more casual</li>
            <li><code>ではありません</code> — more formal</li>
        </ul>
        <p class="mb-4 italic">これはペンじゃありません。→ This is not a pen.</p>
        <p class="mb-4 italic">私は学生ではありません。→ I am not a student.</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Past Tense: でした</h2>
        <p class="mb-4">
            To refer to the past, change <code>です</code> to <code>でした</code>:
        </p>
        <p class="mb-4 italic">昨日は月曜日でした。→ Yesterday was Monday.</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Past Negative: じゃありませんでした / ではありませんでした</h2>
        <p class="mb-4">
            To make a past-tense negative sentence, use:
        </p>
        <ul class="list-disc ml-6 mb-4">
            <li><code>じゃありませんでした</code> — casual past negative</li>
            <li><code>ではありませんでした</code> — formal past negative</li>
        </ul>
        <p class="mb-4 italic">昨日は日曜日じゃありませんでした。→ Yesterday was not Sunday.</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Casual Copula: だ</h2>
        <p class="mb-4">
            In informal speech (especially among friends), the copula <code>だ</code> is used instead of <code>です</code>. However, <code>だ</code> is <strong>not used with adjectives</strong> and should be avoided in polite situations.
        </p>
        <p class="mb-4 italic">彼は先生だ。→ He is a teacher. (casual)</p>

