<section class="w-full max-w-5xl mx-auto px-6 py-12 text-white">
    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Hiragana, Katakana, and Kanji
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Japanese is unique because it uses three writing systems together: <strong>Hiragana</strong>, <strong>Katakana</strong>, and <strong>Kanji</strong>. Each plays a different role in the language.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Hiragana (ひらがな)</h2>
        <p class="mb-4">
            Hiragana is the foundation of Japanese writing. It consists of 46 basic characters, each representing a sound (like "a", "ka", "shi"). It's used for native Japanese words, grammatical elements, and children's books.
        </p>
        <p class="mb-4">
            Mastering Hiragana is essential before moving on to other parts of the language.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Katakana (カタカナ)</h2>
        <p class="mb-4">
            Katakana is another set of 46 characters, but it's mainly used for foreign words, technical terms, loanwords, and emphasis (similar to italics in English).
        </p>
        <p class="mb-4">
            You'll see Katakana when reading menus, advertisements, and words like "コンピューター" (computer) or "パン" (bread).
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Kanji (漢字)</h2>
        <p class="mb-4">
            Kanji are characters borrowed from Chinese. Each Kanji represents an idea or object and usually has multiple readings. Some Kanji represent simple words like "water" (水) or "tree" (木), while others are much more complex.
        </p>
        <p class="mb-4">
            Learning Kanji takes time, but you’ll start with the basics (around 100 for N5 level) and build gradually.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">How They Work Together</h2>
        <p class="mb-4">
            In normal Japanese writing, all three scripts are mixed:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li>Hiragana: for particles and grammar</li>
            <li>Katakana: for foreign or special words</li>
            <li>Kanji: for nouns, verbs, adjectives, and roots of words</li>
        </ul>
        <p>
            You'll quickly get used to the combination by seeing real examples and practicing often.
        </p>
    </div>
</section>
