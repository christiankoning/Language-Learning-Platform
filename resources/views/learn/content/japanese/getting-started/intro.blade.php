            <h2 class="text-2xl font-semibold mt-8 mb-4">The Japanese Writing Systems</h2>
            <p class="mb-4">
                Japanese uses three main scripts:
            </p>
            <ul class="list-disc list-inside mb-6">
                <li><strong>Hiragana (ひらがな)</strong> — A set of phonetic characters used for native Japanese words and grammar elements.</li>
                <li><strong>Katakana (カタカナ)</strong> — Another set of phonetic characters, used mainly for foreign words, technical terms, and names.</li>
                <li><strong>Kanji (漢字)</strong> — Characters borrowed from Chinese, representing ideas, objects, and meanings. Kanji can look intimidating, but you'll learn them step-by-step!</li>
            </ul>

            <h2 class="text-2xl font-semibold mt-8 mb-4">Basic Structure of Japanese Sentences</h2>
            <p class="mb-4">
                Japanese sentence structure is typically Subject-Object-Verb (SOV). Unlike English, where we usually say "I eat sushi", in Japanese it would be more like "I sushi eat" — <strong>私 (I) + 寿司 (sushi) + 食べます (eat)</strong>.
            </p>
            <p class="mb-4">
                Mastering this word order early will help you form natural and correct sentences.
            </p>

