    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Verb Forms: Casual Forms</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Casual verb forms are used in informal speech, such as when talking to friends or family. These forms are also the base for many conjugations in Japanese. Understanding them is essential for both fluency and natural-sounding conversation.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">1. Dictionary Form (る / う / く, etc.)</h2>
        <p class="mb-2">
            The basic form of verbs is called the “dictionary form.” It's the plain present tense used in informal situations.
        </p>
        <p class="italic mb-4">
            食べる (taberu) → to eat<br>
            行く (iku) → to go<br>
            話す (hanasu) → to speak
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">2. Negative Form (〜ない)</h2>
        <p class="mb-2">
            Replace the ending with 〜ない to express a negative informal sentence.
        </p>
        <p class="italic mb-4">
            食べない (tabenai) → don’t eat<br>
            行かない (ikanai) → don’t go
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">3. Past Form (〜た)</h2>
        <p class="mb-2">Used to describe something that happened in the past.</p>
        <p class="italic mb-4">飲んだ (nonda) → drank<br> 書いた (kaita) → wrote</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">4. Negative Past Form (〜なかった)</h2>
        <p class="mb-2">Informal way to say you didn’t do something.</p>
        <p class="italic mb-4">行かなかった (ikanakatta) → didn’t go</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">5. Volitional Form (〜よう / 〜おう)</h2>
        <p class="mb-2">Used to suggest doing something together (Let’s…).</p>
        <p class="italic mb-4">食べよう (tabeyou) → Let’s eat<br> 行こう (ikou) → Let’s go</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">6. Potential Form (〜られる / 〜える)</h2>
        <p class="mb-2">Used to express ability (“can do”).</p>
        <p class="italic mb-4">食べられる (taberareru) → can eat<br> 行ける (ikeru) → can go</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">7. Passive Form (〜られる)</h2>
        <p class="mb-2">Used to express that something is done to the subject (passive voice).</p>
        <p class="italic mb-4">言われる (iwareru) → to be told<br> 読まれる (yomareru) → to be read</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">8. Causative Form (〜させる)</h2>
        <p class="mb-2">Used when making or letting someone do something.</p>
        <p class="italic mb-4">食べさせる (tabesaseru) → to make someone eat</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">9. Conditional Form (〜たら)</h2>
        <p class="mb-2">Used to express “if/when.” Often from the past form + ら.</p>
        <p class="italic mb-4">行ったら (ittara) → if/when (you) go</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">10. て-Form</h2>
        <p class="mb-2">Extremely useful for linking verbs, making requests, or forming progressive/polite forms.</p>
        <p class="italic mb-4">食べて (tabete) → eat (and...)<br> 待って (matte) → wait (and...)</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">11. ちょう / じょう (Command / Invitation)</h2>
        <p class="mb-2">These are casual or regional ways of inviting or commanding, often used playfully or softly.</p>
        <p class="italic mb-4">やっちゃおう (yacchaou) → Let’s just do it!<br> 行っちゃいなよ (icchainayo) → Just go!</p>

        <h2 class="text-2xl font-semibold mt-10 mb-4">Conclusion</h2>
        <p class="mb-4">
            These casual verb forms are foundational for natural Japanese. Many more advanced forms build directly on these, so mastering them will unlock more fluid and nuanced speech.
        </p>

