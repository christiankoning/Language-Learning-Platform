    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Verb Forms: Formal Forms</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Formal verb forms in Japanese are used in polite speech, which is essential for daily conversation, especially with people you don’t know well or in professional situations. This section will help you understand how to properly conjugate and use these forms.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">1. 〜ます (masu) – Present Positive</h2>
        <p class="mb-2">Used to express actions or habits in a polite way.</p>
        <p class="italic mb-4">たべます (tabemasu) → I eat / I will eat</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">2. 〜ません (masen) – Present Negative</h2>
        <p class="mb-2">Used to express that you do not do something, politely.</p>
        <p class="italic mb-4">のみません (nomimasen) → I do not drink</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">3. 〜ました (mashita) – Past Positive</h2>
        <p class="mb-2">Used for actions that happened in the past (politely).</p>
        <p class="italic mb-4">いきました (ikimashita) → I went</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">4. 〜ませんでした (masen deshita) – Past Negative</h2>
        <p class="mb-2">Polite form for saying you didn’t do something.</p>
        <p class="italic mb-4">かきませんでした (kakimasen deshita) → I didn’t write</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">5. 〜たい (tai) – Want to</h2>
        <p class="mb-2">Used to express desire to do something. Attach to the verb stem.</p>
        <p class="italic mb-4">たべたい (tabetai) → I want to eat</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">6. 〜たいですか (tai desu ka) – Do you want to…?</h2>
        <p class="mb-2">Polite way to ask if someone wants to do something.</p>
        <p class="italic mb-4">みたいですか？ (mitai desu ka?) → Do you want to see (it)?</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">7. 〜たいください (tai kudasai) – Please want to… (used rarely)</h2>
        <p class="mb-2">This is uncommon and not grammatically standard. Instead, use the て-form + ください.</p>
        <p class="italic mb-4">✘ のみたいください (Incorrect) → Use: のんでください (nonde kudasai)</p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">8. 〜てもいいですか (te mo ii desu ka) – May I…?</h2>
        <p class="mb-2">Used to politely ask for permission.</p>
        <p class="italic mb-4">ここでしゃしんをとってもいいですか？<br><span class="text-sm">Koko de shashin o totte mo ii desu ka? → May I take a photo here?</span></p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">9. 〜てはいけません (te wa ikemasen) – Must not… / Prohibited</h2>
        <p class="mb-2">Used to say that something is not allowed.</p>
        <p class="italic mb-4">ここでたばこをすってはいけません。<br><span class="text-sm">Koko de tabako o sutte wa ikemasen. → You must not smoke here.</span></p>

        <h2 class="text-2xl font-semibold mt-10 mb-4">Conclusion</h2>
        <p class="mb-4">
            These formal verb forms form the foundation of polite Japanese. Mastering them is essential for respectful and effective communication in most real-life settings.
        </p>

