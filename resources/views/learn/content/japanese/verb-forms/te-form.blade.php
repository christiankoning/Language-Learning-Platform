        <h2 class="text-2xl font-semibold mt-8 mb-4">Common Uses of the Te-Form</h2>

        <h3 class="text-xl font-semibold mt-6 mb-2">1. Connecting Actions</h3>
        <p class="mb-4">
            You can link multiple actions together like “and then.”
        </p>
        <p class="italic mb-4">
            学校へ行って、本を読みます。<br>
            (gakkou e itte, hon o yomimasu.) → I go to school and read a book.
        </p>

        <h3 class="text-xl font-semibold mt-6 mb-2">2. Making Requests</h3>
        <p class="mb-4">
            Combine with ください (kudasai) to politely ask someone to do something.
        </p>
        <p class="italic mb-4">見てください。→ Please look.</p>

        <h3 class="text-xl font-semibold mt-6 mb-2">3. Giving and Receiving Help</h3>
        <p class="mb-4">
            Used with あげる / くれる / もらう to express giving/receiving actions.
        </p>
        <p class="italic mb-4">宿題を手伝ってくれる？ → Can you help me with homework?</p>

        <h3 class="text-xl font-semibold mt-6 mb-2">4. Permissions</h3>
        <p class="mb-4">Use 〜てもいいですか to ask for permission.</p>
        <p class="italic mb-4">ここに座ってもいいですか？ → May I sit here?</p>

        <h3 class="text-xl font-semibold mt-6 mb-2">5. Prohibitions</h3>
        <p class="mb-4">Use 〜てはいけません to say something is not allowed.</p>
        <p class="italic mb-4">ここでたばこを吸ってはいけません。→ You must not smoke here.</p>

        <h2 class="text-2xl font-semibold mt-10 mb-4">Conclusion</h2>
        <p class="mb-4">
            The te-form is at the heart of Japanese grammar. Once you understand how it works and how to use it, you'll unlock a wide range of expressions and sentence structures.
