            </table>
        </div>

        <h2 class="text-2xl font-semibold mt-8 mb-4">When to Use the Plain Form</h2>
        <ul class="list-disc ml-6 mb-4">
            <li>Talking with friends or family</li>
            <li>Writing personal diaries or blogs</li>
            <li>Speaking to children or people of equal/lower status</li>
            <li>Constructing casual questions and responses</li>
            <li>Embedding in longer grammatical structures</li>
        </ul>

