<section class="w-full max-w-5xl mx-auto px-6 py-12 text-white">
    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">Verb Forms: Plain Form</h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            The plain form (or casual form) is used in informal speech and writing, such as conversations with friends, diary entries, or casual online messages. It's also the base form used for conjugation in many grammatical patterns.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">What Is the Plain Form?</h2>
        <p class="mb-4">
            In Japanese, most verbs have a polite form (ます-form) and a plain (dictionary) form. For example:
        </p>
        <ul class="list-disc ml-6 mb-4">
            <li><strong>Polite:</strong> 食べます (tabemasu)</li>
            <li><strong>Plain:</strong> 食べる (taberu)</li>
        </ul>

