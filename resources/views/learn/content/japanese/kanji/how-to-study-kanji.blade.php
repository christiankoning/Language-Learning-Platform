<section class="w-full max-w-5xl mx-auto px-6 py-12 text-white">
    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        How to Study Kanji
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Kanji can seem overwhelming at first — there are thousands of characters! But you don’t need to learn them all at once. With the right method and mindset, you can build a solid Kanji foundation and grow steadily from there.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">1. Focus on the JLPT Levels</h2>
        <p class="mb-4">
            A good starting point is to learn the Kanji that appear in the Japanese Language Proficiency Test (JLPT). Here’s how many you’ll need at each level:
        </p>

        <ul class="list-disc list-inside mb-6">
            <li><strong>N5:</strong> ~100 basic Kanji (used in simple words and sentences)</li>
            <li><strong>N4:</strong> ~300 Kanji total (adds basic verbs, days, common signs)</li>
            <li><strong>N3:</strong> ~650 Kanji total (starts real fluency)</li>
        </ul>

        <p class="mb-4">
            These levels are perfect goals, and we’ll guide you through learning the Kanji you need.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">2. Learn Meaning, Reading, and Usage Together</h2>
        <p class="mb-4">
            Don’t just memorize shapes — each Kanji has three essential parts:
        </p>

        <ul class="list-disc list-inside mb-6">
            <li><strong>Meaning:</strong> What does the Kanji represent?</li>
            <li><strong>Reading:</strong> How is it pronounced? (On’yomi / Kun’yomi)</li>
            <li><strong>Vocabulary:</strong> What words is it used in?</li>
        </ul>

        <p class="mb-4">
            For example:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>Kanji:</strong> 食</li>
            <li><strong>Meaning:</strong> Eat</li>
            <li><strong>On’yomi:</strong> ショク (shoku)</li>
            <li><strong>Kun’yomi:</strong> た(べる) (taberu)</li>
            <li><strong>Word examples:</strong> 食べる (to eat), 食事 (meal), 朝食 (breakfast)</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">3. Practice Writing (Even Just a Little)</h2>
        <p class="mb-4">
            Writing helps you remember the structure and stroke order. Even if you're mainly learning for reading, writing makes recognition easier.
        </p>

        <ul class="list-disc list-inside mb-6">
            <li>Use grid paper to practice stroke balance.</li>
            <li>Say the reading aloud while you write.</li>
            <li>Write new Kanji 5–10 times when you first learn them.</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">4. Use Flashcards or Apps (Spaced Repetition)</h2>
        <p class="mb-4">
            Spaced repetition (SRS) is a powerful way to review Kanji over time. You’ll see difficult Kanji more often, and easy ones less often.
        </p>

        <ul class="list-disc list-inside mb-6">
            <li>Try using Anki, Wanikani, or a gamified platform like this one.</li>
            <li>Make custom flashcards with the Kanji, meaning, reading, and example word.</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">5. Group Similar Kanji</h2>
        <p class="mb-4">
            Many Kanji share parts (called radicals). You can learn new Kanji faster if you notice the patterns.
        </p>

        <ul class="list-disc list-inside mb-6">
            <li><strong>水</strong> (water) appears in: 海 (sea), 酒 (alcohol), 河 (river)</li>
            <li><strong>心</strong> (heart) appears in: 忘 (forget), 悲 (sad), 情 (emotion)</li>
        </ul>

        <p class="mb-4">
            Learning radicals also helps you guess meaning and pronunciation for unfamiliar Kanji.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">6. Study in Context</h2>
        <p class="mb-4">
            Kanji are best remembered in real vocabulary and sentences. If you only study them in isolation, they’re easy to forget.
        </p>

        <ul class="list-disc list-inside mb-6">
            <li>Use beginner texts with furigana (Kana above the Kanji)</li>
            <li>Look up Kanji from anime, books, or games you like</li>
            <li>Review words from lessons, not just single characters</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">7. Be Patient and Consistent</h2>
        <p class="mb-4">
            You don't need to rush. Just a few Kanji each day will lead to huge progress over time. Most Japanese kids take years to learn them all — and you can too.
        </p>

        <ul class="list-disc list-inside mb-6">
            <li>Learn 3–5 new Kanji per day if you're consistent.</li>
            <li>Review past Kanji with quizzes, writing, or games.</li>
            <li>Track your progress to stay motivated.</li>
        </ul>

        <p class="mb-4">
            Ready to begin? Let’s look at the Kanji you need for JLPT N5!
        </p>
    </div>
</section>
