<section class="w-full max-w-5xl mx-auto px-6 py-12 text-white">
    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        On'yomi vs Kun'yomi
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Every Kanji has one or more readings — ways to pronounce it. These readings are split into two categories:
        </p>

        <ul class="list-disc list-inside mb-6">
            <li><strong>On’yomi (音読み)</strong> – The “Chinese” reading, borrowed from Chinese pronunciation when Kanji were imported.</li>
            <li><strong>Kun’yomi (訓読み)</strong> – The “native Japanese” reading, used when Kanji are paired with native Japanese words.</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">How to Know Which Reading to Use?</h2>
        <p class="mb-4">
            The context tells you which reading to use:
        </p>

        <ul class="list-disc list-inside mb-6">
            <li><strong>On’yomi</strong> is mostly used in <strong>compound words (熟語 - jukugo)</strong>, where two or more Kanji are combined. Example: 学校 (がっこう, school)</li>
            <li><strong>Kun’yomi</strong> is often used when a Kanji appears <strong>alone</strong> or with Hiragana. Example: 山 (やま, mountain)</li>
        </ul>

