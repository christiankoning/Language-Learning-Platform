        <ul class="list-disc list-inside mb-6">
            <li>Many Kanji have <strong>more than one</strong> On'yomi or Kun'yomi reading.</li>
            <li>Some characters are almost always read with just one type (e.g., 学 is usually On’yomi).</li>
            <li>Practice and exposure will help you memorize the most common readings and patterns.</li>
