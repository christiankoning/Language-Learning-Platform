    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Introduction to Kanji
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Kanji (漢字) are logographic characters borrowed from Chinese that form a core part of the Japanese writing system. While Hiragana and Katakana represent sounds, each Kanji represents a meaning or concept — often with multiple readings depending on context.
        </p>

        <p class="mb-4">
            Learning Kanji may feel intimidating at first, but with the right approach, it becomes manageable — and even fun. You'll encounter Kanji everywhere: signs, menus, newspapers, messages, and even on TV.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Why Learn Kanji?</h2>
        <ul class="list-disc list-inside mb-6">
            <li><strong>Comprehension:</strong> Over 70% of written Japanese uses Kanji.</li>
            <li><strong>Clarity:</strong> Kanji helps distinguish between homophones that sound the same in Hiragana.</li>
            <li><strong>Efficiency:</strong> A single Kanji can express a full idea that would otherwise take multiple syllables to write.</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">What Does a Kanji Look Like?</h2>
        <p class="mb-4">
            Kanji characters can be simple like <strong>一 (one)</strong> or complex like <strong>職 (profession)</strong>. Each character is made of smaller components called <strong>radicals</strong>, which help you understand meaning and guess pronunciation.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">How Many Kanji Are There?</h2>
        <p class="mb-4">
            There are thousands of Kanji in total, but the good news is:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>2,136</strong> are officially recognized as "Jouyou Kanji" (for daily use)</li>
            <li><strong>800–1,000</strong> are enough to read most newspapers, websites, and books aimed at adults</li>
            <li><strong>300–400</strong> cover most of what you’ll encounter in daily life and JLPT N5/N4</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">How Kanji Works in Japanese</h2>
        <p class="mb-4">
            Each Kanji has:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>Meaning</strong> — what the character represents (e.g., 日 means "sun" or "day")</li>
            <li><strong>Readings</strong> — how it is pronounced depending on context. This includes:
                <ul class="list-disc ml-6 mt-2">
                    <li><strong>On'yomi (音読み)</strong> – the Chinese-based reading</li>
                    <li><strong>Kun'yomi (訓読み)</strong> – the native Japanese reading</li>
                </ul>
            </li>
        </ul>

