    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Kanji Stroke Order Rules
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Writing Kanji correctly means following the correct stroke order. This isn’t just about neatness — stroke order helps with character recognition, flow, and even proper handwriting input on devices.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Why Stroke Order Matters</h2>
        <ul class="list-disc list-inside mb-6">
            <li>It reinforces the structure of the character.</li>
            <li>It helps with reading and remembering similar-looking characters.</li>
            <li>It is expected in schools and tests (like the JLPT or handwriting exams).</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Basic Stroke Order Rules</h2>
        <ul class="list-disc list-inside mb-6">
            <li><strong>Top to bottom</strong> — e.g., 三 is written from the top line to the bottom.</li>
            <li><strong>Left to right</strong> — e.g., 木 starts with the left stroke, then the right.</li>
            <li><strong>Horizontal before vertical</strong> — e.g., 十 starts with the horizontal line.</li>
            <li><strong>Outside before inside</strong> — e.g., for 回, draw the outer box first, then the inside.</li>
            <li><strong>Inside before closing</strong> — e.g., for 国, write the interior first, then the enclosing box.</li>
            <li><strong>Center before sides (symmetrical)</strong> — e.g., 水 starts with the center stroke.</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Example: 水 (water)</h2>
        <p class="mb-4">
            This character has 4 strokes. The order is:
        </p>
        <ol class="list-decimal list-inside mb-6">
            <li>Vertical stroke (center)</li>
            <li>Left diagonal stroke</li>
            <li>Right diagonal stroke</li>
            <li>Short center-bottom tick</li>
        </ol>

        <p class="mb-4">
            It’s a good habit to write Kanji by hand regularly to internalize the correct stroke pattern.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Practice Tips</h2>
        <ul class="list-disc list-inside mb-6">
            <li>Use Kanji notebooks with square grids to help alignment.</li>
            <li>Practice slowly and neatly — speed comes later.</li>
            <li>Watch stroke order animations or practice apps (like Jisho.org or Kanshudo).</li>
        </ul>

