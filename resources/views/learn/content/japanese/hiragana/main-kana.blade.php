        <h2 class="text-2xl font-semibold mt-8 mb-4">Tips for Learning</h2>
        <ul class="list-disc list-inside mb-6">
            <li>Learn one row (like a-line, ka-line, etc.) at a time — don't rush!</li>
            <li>Practice writing them out by hand — muscle memory helps a lot.</li>
            <li>Say each character aloud as you write it to reinforce pronunciation and shape.</li>
            <li>Use flashcards and simple quizzes to test yourself regularly.</li>
        </ul>

        <p class="mb-4">
