        Hiragana: Combination Kana (Yōon)
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Combination Kana (known as <strong>Yōon (拗音)</strong> in Japanese) are special sounds created by combining a consonant kana with a small version of <strong>や (ya)</strong>, <strong>ゆ (yu)</strong>, or <strong>よ (yo)</strong>.
        </p>

        <p class="mb-4">
            Instead of pronouncing them separately, they blend together into a single, smooth sound.
            For example, き (ki) + small や (ゃ) = きゃ (kya).
        </p>

