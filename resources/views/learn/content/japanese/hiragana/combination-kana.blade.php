        <h2 class="text-2xl font-semibold mt-8 mb-4">Important Tips</h2>
        <ul class="list-disc list-inside mb-6">
            <li>The small ゃ, ゅ, and ょ must stay small. If they are written big, it's wrong!</li>
            <li>Practice by reading and writing full words, not just the syllables individually.</li>
            <li>Many common words use combination kana, so mastering them is very important.</li>
        </ul>

        <p class="mb-4">
            Combination kana may feel strange at first, but you’ll quickly get used to them with practice!
