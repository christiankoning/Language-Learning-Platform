        <h2 class="text-2xl font-semibold mt-8 mb-4">Tips for Remembering Dakuten & Handakuten</h2>
        <ul class="list-disc list-inside mb-6">
            <li>Think of Dakuten (゛) as adding "voice" to a consonant.</li>
            <li>Think of Handakuten (゜) as a "pop" sound for H-row kana.</li>
            <li>Practice writing both normal and modified versions side-by-side to feel the difference!</li>
        </ul>

        <p class="mb-4">
            These modifications are essential for natural Japanese pronunciation and vocabulary. Practice and repeat them often!
