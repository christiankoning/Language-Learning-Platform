    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Hiragana: Small っ (Tsu) & Long Vowels
    </h1>

    <div class="prose prose-invert max-w-none">
        <h2 class="text-2xl font-semibold mt-8 mb-4">Small っ (Tsu) — Indicating a Double Consonant</h2>

        <p class="mb-4">
            The small <strong>っ</strong> (called <em>chiisai tsu</em> or "small tsu") is used to create a slight pause or double the next consonant sound.
            It’s essential for expressing correct pronunciation and meaning.
        </p>

        <h3 class="text-xl font-semibold mt-6 mb-2">Examples:</h3>
        <ul class="list-disc list-inside mb-6">
            <li>さか <strong>(saka)</strong> = hill</li>
            <li>さっか <strong>(sakka)</strong> = writer, author</li>
            <li>きて <strong>(kite)</strong> = come!</li>
            <li>きって <strong>(kitte)</strong> = postage stamp</li>
        </ul>

        <p class="mb-4">
            Notice how the double consonant changes the meaning entirely!
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">How to Pronounce っ</h2>

        <p class="mb-4">
            When you see a small っ, make a very short pause before pronouncing the next consonant strongly.
        </p>
        <p class="mb-4 italic">
            It's like cutting the breath slightly before continuing the word.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Long Vowels (ー)</h2>

        <p class="mb-4">
            Japanese has long vowels, where a vowel sound is stretched longer than normal.
            In Hiragana, long vowels are usually formed by adding another vowel kana after the first.
        </p>

        <h3 class="text-xl font-semibold mt-6 mb-2">Rules for Long Vowels:</h3>
        <ul class="list-disc list-inside mb-6">
            <li><strong>A</strong> sounds are extended by adding あ (ああ)</li>
            <li><strong>I</strong> sounds are extended by adding い (いい)</li>
            <li><strong>U</strong> sounds are extended by adding う (くう)</li>
            <li><strong>E</strong> sounds are sometimes extended by い (せい for example)</li>
            <li><strong>O</strong> sounds are often extended by う (こう)</li>
        </ul>

        <h3 class="text-xl font-semibold mt-6 mb-2">Examples:</h3>
        <ul class="list-disc list-inside mb-6">
            <li>おかあさん (okaasan) — mother (long あ sound)</li>
            <li>おじいさん (ojiisan) — grandfather (long い sound)</li>
            <li>くうこう (kuukou) — airport (long う sound)</li>
            <li>せんせい (sensei) — teacher (long え sound extended by い)</li>
            <li>おとうさん (otousan) — father (long お sound extended by う)</li>
        </ul>

        <p class="mb-4">
            Long vowels are <strong>very important</strong> in Japanese!
            Changing a long vowel to a short one can completely change the meaning of a word.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Important Notes</h2>
        <ul class="list-disc list-inside mb-6">
            <li>In Katakana, the long vowel mark ー (chōonpu) is used instead of extra kana.</li>
            <li>In Hiragana, you must know which vowel extension is correct.</li>
            <li>Practice listening carefully to hear the difference between short and long vowels!</li>
        </ul>

        <p class="mb-4">
            Mastering small っ and long vowels is a huge step toward sounding natural in Japanese!
        </p>
    </div>
