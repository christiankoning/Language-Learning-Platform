<section class="w-full max-w-5xl mx-auto px-6 py-12 text-white">
    <h1 class="text-3xl font-bold mb-6 drop-shadow-md">
        Hiragana: Pronunciation
    </h1>

    <div class="prose prose-invert max-w-none">
        <p class="mb-4">
            Hiragana pronunciation is generally consistent and simple compared to English. Each character represents exactly one sound, and the pronunciation rarely changes. Here's what you need to know to pronounce Hiragana correctly:
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Japanese Vowels</h2>
        <p class="mb-4">
            Japanese has five vowels:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>A (あ)</strong> — pronounced like "ah" in "car"</li>
            <li><strong>I (い)</strong> — pronounced like "ee" in "see"</li>
            <li><strong>U (う)</strong> — pronounced like "oo" in "food", but shorter and with less lip rounding</li>
            <li><strong>E (え)</strong> — pronounced like "e" in "get"</li>
            <li><strong>O (お)</strong> — pronounced like "o" in "more" but shorter</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Important Pronunciation Notes</h2>
        <ul class="list-disc list-inside mb-6">
            <li><strong>All sounds are "pure".</strong> Vowels don't glide or change into other sounds.</li>
            <li><strong>No stressed syllables.</strong> Unlike English, Japanese words have a flatter, more even rhythm.</li>
            <li><strong>The "R" sound</strong> (ら、り、る、れ、ろ) is between an English "R" and "L". It's a quick tap of the tongue, almost like a soft "D" sound.</li>
        </ul>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Silent and Dropped Sounds</h2>
        <p class="mb-4">
            In normal speech, some vowels — especially <strong>U (う)</strong> and sometimes <strong>I (い)</strong> — are dropped or barely pronounced in certain words.
        </p>
        <p class="mb-4">Examples:</p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>です (desu)</strong> → Often pronounced like "dess" (the "u" is very quiet or silent)</li>
            <li><strong>好き (suki)</strong> → Often pronounced like "ski" (the "u" is very faint)</li>
            <li><strong>した (shita)</strong> → No change; all sounds are pronounced.</li>
        </ul>
        <p class="mb-4">
            This phenomenon is called <strong>unvoiced vowels</strong> (無声母音, musei boin). It happens naturally in fast or casual speech but doesn't change the spelling.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Lengthened Vowels</h2>
        <p class="mb-4">
            Some vowels are intentionally held longer, usually by writing two vowels in a row:
        </p>
        <ul class="list-disc list-inside mb-6">
            <li><strong>おばあさん (obaasan)</strong> — "Grandmother" (the "あ" is lengthened)</li>
            <li><strong>おおきい (ookii)</strong> — "Big" (both "お" and "い" are lengthened)</li>
        </ul>
        <p class="mb-4">
            Lengthening a vowel changes the meaning of a word, so it's important to pronounce them correctly.
        </p>

        <h2 class="text-2xl font-semibold mt-8 mb-4">Practice Tips</h2>
        <ul class="list-disc list-inside mb-6">
            <li>Practice the vowels slowly and clearly: あ、い、う、え、お.</li>
            <li>Repeat simple words out loud to get used to Japanese rhythm.</li>
            <li>Listen carefully to native speakers and imitate their pronunciation.</li>
        </ul>

        <p>
            Once you're comfortable with pronunciation, it's time to start learning the Hiragana characters themselves!
        </p>
    </div>
</section>
